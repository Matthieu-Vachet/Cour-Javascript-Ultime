// setTimeout affiche une alerte au bout de 3 secondes
let timer = setTimeout("alert('Bonjour')", 3000);
// clearTimeout(timer); // On stop le setTimeout
clearTimeout(timer);

// setInterval affiche une alerte toutes les 5 secondes
let interval = setInterval("alert('Bonjour')", 5000);
// clearInterval(interval); // On stop le setInterval
clearInterval(interval);