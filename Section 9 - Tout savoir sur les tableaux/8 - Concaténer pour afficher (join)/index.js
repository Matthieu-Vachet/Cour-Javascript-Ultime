let monTableauSimple = ["un", "deux", "trois", "quatre"]; // index 0, 1, 2

console.log(monTableauSimple.join()); // Resultat : un,deux,trois,quatre

console.log(monTableauSimple.join(' / ')); // Resultat : un / deux / trois / quatre




let monTableau2D = [
        ["Mark", "Jeff", "Bill"], // Tableau 0 / index 0,1,2
        ["Zuckerberg", "Bezos", "Gates"], // Tableau 1 / index 0,1,2
];

console.log(monTableau2D.join()); // Resultat : Mark,Jeff,Bill,Zuckerberg,Bezos,Gates

console.log(monTableau2D.join(' / ')); // Resultat : Mark,Jeff,Bill / Zuckerberg,Bezos,Gates


